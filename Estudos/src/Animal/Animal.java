package Animal;

public class Animal {
    private String alimentary;

    private String locomotive;

    public String getAlimentary(){
        return alimentary;
    }

    public String getLocomotive(){
        return locomotive;
    }

    public void setLocomotive(String locomotive){
        this.locomotive = locomotive;
    }

    public void setAlimentary(String alimentary){
        this.alimentary = alimentary;
    }

}
